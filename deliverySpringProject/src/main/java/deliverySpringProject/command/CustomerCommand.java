package deliverySpringProject.command;

import java.util.Date;

public class CustomerCommand {
	String customerName;
	String customerId;
	String customerPw;
	String customerPwCon;
	String customerPhone;
	String customerAddr;
	String customerAddrDetail;
	Date customerBirth;
	String customerEmail;
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getCustomerPw() {
		return customerPw;
	}
	public void setCustomerPw(String customerPw) {
		this.customerPw = customerPw;
	}
	public String getCustomerPwCon() {
		return customerPwCon;
	}
	public void setCustomerPwCon(String customerPwCon) {
		this.customerPwCon = customerPwCon;
	}
	public String getCustomerPhone() {
		return customerPhone;
	}
	public void setCustomerPhone(String customerPhone) {
		this.customerPhone = customerPhone;
	}
	public String getCustomerAddr() {
		return customerAddr;
	}
	public void setCustomerAddr(String customerAddr) {
		this.customerAddr = customerAddr;
	}
	public String getCustomerAddrDetail() {
		return customerAddrDetail;
	}
	public void setCustomerAddrDetail(String customerAddrDetail) {
		this.customerAddrDetail = customerAddrDetail;
	}
	public Date getCustomerBirth() {
		return customerBirth;
	}
	public void setCustomerBirth(Date customerBirth) {
		this.customerBirth = customerBirth;
	}
	public String getCustomerEmail() {
		return customerEmail;
	}
	public void setCustomerEmail(String customerEmail) {
		this.customerEmail = customerEmail;
	}
	
}
